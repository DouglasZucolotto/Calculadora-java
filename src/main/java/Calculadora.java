public class Calculadora {
    int valor1;
    int valor2;

    public int soma() {
            return valor1 + valor2;
    }

    public double Pontos() {

        return (valor1 / valor2);
    }

    public int Divisao() {
        return (valor1 / valor2);
    }

    public int Multiplicacao() {
        return (valor1 * valor2);
    }
}
